package com.yc.model;

public class ShopOrderSearch {
	
	private String nameOfGoods;
	
	private String paymentDateLeft;
	
	private String paymentDateRight;
	
	private String orderUserName;
	
	private String orderStatusFrom;
	
	private String orderFormID;

	public String getNameOfGoods() {
		return nameOfGoods;
	}

	public void setNameOfGoods(String nameOfGoods) {
		this.nameOfGoods = nameOfGoods;
	}

	public String getPaymentDateLeft() {
		return paymentDateLeft;
	}

	public void setPaymentDateLeft(String paymentDateLeft) {
		this.paymentDateLeft = paymentDateLeft;
	}

	public String getPaymentDateRight() {
		return paymentDateRight;
	}

	public void setPaymentDateRight(String paymentDateRight) {
		this.paymentDateRight = paymentDateRight;
	}

	public String getOrderUserName() {
		return orderUserName;
	}

	public void setOrderUserName(String orderUserName) {
		this.orderUserName = orderUserName;
	}

	public String getOrderStatusFrom() {
		return orderStatusFrom;
	}

	public void setOrderStatusFrom(String orderStatusFrom) {
		this.orderStatusFrom = orderStatusFrom;
	}

	public String getOrderFormID() {
		return orderFormID;
	}

	public void setOrderFormID(String orderFormID) {
		this.orderFormID = orderFormID;
	}
	
}
